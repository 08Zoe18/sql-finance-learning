-- 3. 财务分析常用查询（每个查询前都有中文注释说明用途）
-- =====================================================

-- 3.1 查看所有原始数据（核对数据是否插入成功）
SELECT * FROM sales;

-- 3.2 按产品类别汇总销售额（类似利润表中的“营业收入按产品大类拆分”）
SELECT 产品类别, SUM(金额) AS 总销售额
FROM sales
GROUP BY 产品类别;

-- 3.3 按销售区域统计销售额（分析哪个区域贡献最大）
SELECT 销售区域, SUM(金额) AS 区域销售额
FROM sales
GROUP BY 销售区域
ORDER BY 区域销售额 DESC;   -- 降序排列，方便看出第一名

-- 3.4 找出销售额最高的前3个产品（核心产品识别）
SELECT 产品名称, SUM(金额) AS 总销售额
FROM sales
GROUP BY 产品名称
ORDER BY 总销售额 DESC
LIMIT 3;

-- 3.5 按客户类型统计订单数和总销售额（分析企业客户 vs 个人客户）
SELECT 客户类型, 
       COUNT(*) AS 订单数, 
       SUM(金额) AS 总销售额
FROM sales
GROUP BY 客户类型;

-- 3.6 每日销售趋势（用于时间序列分析，预测未来现金流）
SELECT 日期, SUM(金额) AS 日销售额
FROM sales
GROUP BY 日期
ORDER BY 日期;

-- 3.7 筛选华东区域且金额超过5000的大额订单（异常或重点订单）
SELECT * FROM sales
WHERE 销售区域 = '华东' AND 金额 > 5000;

-- 3.8 按销售员统计总业绩并排名（用于绩效考核）
SELECT 销售员, SUM(金额) AS 总业绩
FROM sales
GROUP BY 销售员
ORDER BY 总业绩 DESC;

-- 3.9 筛选总销售额超过5000的产品类别（用HAVING过滤分组后的结果）
SELECT 产品类别, SUM(金额) AS 总销售额
FROM sales
GROUP BY 产品类别
HAVING 总销售额 > 5000;

-- 3.10 使用CASE语句对订单金额进行分层（客户价值分层常用）
SELECT 产品名称, 金额,
    CASE 
        WHEN 金额 > 5000 THEN '大单'
        WHEN 金额 BETWEEN 1000 AND 5000 THEN '中单'
        ELSE '小单'
    END AS 订单类型
FROM sales;

