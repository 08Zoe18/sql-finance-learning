-- =====================================================
-- 2. INNER JOIN（内连接）
-- 作用：只返回两个表中能匹配上的行，常用于合并相关表信息。
-- 本例计算每个销售记录的单位毛利和总毛利。
-- =====================================================
SELECT 
    s.日期,
    s.产品名称,
    s.销售数量,
    s.单价,
    s.金额,
    p.单位成本,
    p.供应商,
    (s.单价 - p.单位成本) AS 单位毛利,                     -- 计算单位毛利
    (s.单价 - p.单位成本) * s.销售数量 AS 总毛利            -- 计算总毛利
FROM sales s
INNER JOIN products p ON s.产品名称 = p.产品名称;
-- 注意：sales 中如果有产品名称在 products 中不存在，该行会被忽略


-- =====================================================
-- 3. LEFT JOIN（左连接）
-- 作用：返回左表（sales）的所有行，右表不匹配的部分为 NULL。
-- 本例可用于检查 sales 中哪些产品在 products 表中缺失。
-- =====================================================
SELECT 
    s.日期,
    s.产品名称,
    s.销售数量,
    s.单价,
    s.金额,
    p.单位成本,
    p.供应商
FROM sales s
LEFT JOIN products p ON s.产品名称 = p.产品名称;
-- 如果 p.单位成本 或 p.供应商 为 NULL，说明该产品在 products 表中没有对应记录


-- =====================================================
-- 4. 子查询（Subquery）
-- 4.1 标量子查询：返回单个值，用作外层查询的筛选条件
-- 作用：找出销售额高于平均值的记录
-- =====================================================
-- 先计算平均销售额
SELECT AVG(金额) FROM sales;

-- 使用子查询筛选
SELECT * FROM sales
WHERE 金额 > (SELECT AVG(金额) FROM sales);


-- =====================================================
-- 4.2 相关子查询：内外层关联，对每一行执行一次子查询
-- 作用：找出每个产品类别中金额最高的记录
-- =====================================================
SELECT s1.产品类别, s1.产品名称, s1.金额
FROM sales s1
WHERE s1.金额 = (
    SELECT MAX(s2.金额)
    FROM sales s2
    WHERE s2.产品类别 = s1.产品类别      -- 内层关联外层
);
-- 注意：如果同一类别有多个相同最大金额，这些记录都会被返回


-- =====================================================
-- 5. 视图（View）
-- 作用：将常用查询保存为虚拟表，简化后续操作
-- =====================================================

-- 创建视图：销售毛利视图（包含单位毛利）
CREATE VIEW 销售毛利视图 AS
SELECT 
    s.日期,
    s.产品名称,
    s.销售数量,
    s.单价,
    s.金额,
    p.单位成本,
    (s.单价 - p.单位成本) AS 单位毛利
FROM sales s
LEFT JOIN products p ON s.产品名称 = p.产品名称;

-- 查询视图：筛选单位毛利大于500的记录
SELECT * FROM 销售毛利视图
WHERE 单位毛利 > 500;

