/*
【SELECT】         查询数据，后面跟要看的列名（* 表示所有列）
【FROM】           指定从哪张表查询
【WHERE】          筛选条件，比如 销售区域 = '华东'
【GROUP BY】       按某列分组汇总，常与SUM/COUNT/AVG等一起用
【HAVING】         对分组后的结果进行筛选（类似WHERE，但用在GROUP BY之后）
【ORDER BY】       排序，ASC升序（默认），DESC降序
【LIMIT】          限制返回的行数，比如只看前3名
【CASE WHEN】      条件判断，类似于Excel的IF函数，可以根据金额分类
【SUM/COUNT】      聚合函数，SUM求和，COUNT计数

这些语法在财务分析中的典型应用：
- 按产品类别、区域、客户类型统计收入 → 分析业务结构
- 销售员业绩排名 → 绩效评估
- 每日销售额趋势 → 现金流预测
- 订单金额分层 → 客户价值分析
*/