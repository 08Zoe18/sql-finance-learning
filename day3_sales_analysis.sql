-- 1. 创建表结构（如果之前已存在，可先删除再重建）
DROP TABLE IF EXISTS sales;

CREATE TABLE sales (
    id INT AUTO_INCREMENT PRIMARY KEY,          -- 自增主键，无实际业务意义
    日期 DATE,                                   -- 销售日期
    产品类别 VARCHAR(20),                         -- 产品大类（电子产品/办公用品）
    产品名称 VARCHAR(50),                         -- 具体产品名称
    客户类型 VARCHAR(10),                          -- 企业/个人
    销售区域 VARCHAR(10),                          -- 华东/华南/华北
    销售数量 INT,                                 -- 销售件数
    单价 DECIMAL(10,2),                           -- 产品单价
    金额 DECIMAL(10,2),                           -- 销售金额（=数量×单价）
    销售员 VARCHAR(10)                            -- 销售员姓名
);

